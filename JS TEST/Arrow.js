// Arrow fuction merupakan penyederhanaan penulisan sebuah fuction
// di javascript

//contoh 1
const fungsi = function(a, b) {
return a + b;
}
console.log(fungsi(3,3)); //6

//contoh sederhana ke 2
const fungsii = (a, b) => {
  return a + b;
};
console.log(fungsii(3,3));

//contoh sederhana atau lebih sederhana ke 3

const tambah = (a, b) => a + b;
console.log(tambah(3,3));


